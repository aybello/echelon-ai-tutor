export function getQuestionsByModule(module: string): Question[] {
  return QUESTIONS.filter(q => q.module === module);
}

export function getNextQuestion(
  history: HistoryEntry[],
  currentId?: number
): Question | null {
  const answeredIds = new Set(history.map(h => h.question.id));
  const unanswered = QUESTIONS.filter(q => !answeredIds.has(q.id));
  if (unanswered.length === 0) return null;
  // Prioritize wrong answers and low confidence
  const wrongIds = new Set(history.filter(h => !h.correct).map(h => h.question.id));
  const lowConfidence = history.filter(h => h.confidence <= 2).map(h => h.question.id);
  const priority = unanswered.filter(q => wrongIds.has(q.id) || lowConfidence.includes(q.id));
  const pool = priority.length > 0 ? priority : unanswered;
  return pool[Math.floor(Math.random() * pool.length)];
}

export function getPatternInsights(history: HistoryEntry[]): string[] {
  const insights: string[] = [];
  if (history.length < 5) return insights;
  const moduleStats: Record<string, { correct: number; total: number }> = {};
  for (const entry of history) {
    const m = entry.question.module;
    if (!moduleStats[m]) moduleStats[m] = { correct: 0, total: 0 };
    moduleStats[m].total++;
    if (entry.correct) moduleStats[m].correct++;
  }
  for (const [module, stats] of Object.entries(moduleStats)) {
    const pct = (stats.correct / stats.total) * 100;
    if (pct < 60 && stats.total >= 3) {
      insights.push(`Focus on ${module} — you're at ${Math.round(pct)}% accuracy`);
    }
  }
  return insights;
}

