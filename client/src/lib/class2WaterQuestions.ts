export function getNextQuestion(
  history: Array<{ questionId: number; correct: boolean; module: string }>,
  currentModule: string | null
): Question | null {
  const usedIds = new Set(history.map((h) => h.questionId));
  const pool = QUESTIONS.filter((q) => {
    if (usedIds.has(q.id)) return false;
    if (currentModule && q.module !== currentModule) return false;
    return true;
  });
  if (pool.length === 0) return null;
  return pool[Math.floor(Math.random() * pool.length)];
}

export function getPatternInsights(
  history: Array<{ questionId: number; correct: boolean; module: string }>
): Array<{ module: string; correct: number; total: number; pct: number }> {
  const stats: Record<string, { correct: number; total: number }> = {};
  for (const h of history) {
    if (!stats[h.module]) stats[h.module] = { correct: 0, total: 0 };
    stats[h.module].total++;
    if (h.correct) stats[h.module].correct++;
  }
  return Object.entries(stats).map(([module, s]) => ({
    module,
    correct: s.correct,
    total: s.total,
    pct: Math.round((s.correct / s.total) * 100),
  }));
}

