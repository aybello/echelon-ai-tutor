/**
 * Returns questions filtered by module(s).
 */
export function getWQAQuestions(modules?: WQAModule[]): WQAQuestion[] {
  if (!modules || modules.length === 0) return WQA_QUESTIONS;
  return WQA_QUESTIONS.filter((q) => modules.includes(q.module as WQAModule));
}

/**
 * Returns a shuffled selection of n questions, optionally filtered by module.
 */
export function selectWQAQuestions(n: number, modules?: WQAModule[]): WQAQuestion[] {
  const pool = getWQAQuestions(modules);
  const shuffled = [...pool].sort(() => Math.random() - 0.5);
  return shuffled.slice(0, Math.min(n, shuffled.length));
}

/**
 * Returns the count of questions per module.
 */
export function getWQAModuleCounts(): Record<WQAModule, number> {
  const counts = {} as Record<WQAModule, number>;
  for (const mod of WQA_MODULES) {
    counts[mod] = WQA_QUESTIONS.filter((q) => q.module === mod).length;
  }
  return counts;
}
